/** Question 102: Absolute difference between two numbers
Calculate and log the absolute difference between the number -5 and 5.*/

//-----------------------------------------------------------------------

// Using Math.abs() to calculate the absolute difference between -5 and 5
const num1 = -5;
const num2 = 5;
const absoluteDifference = Math.abs(num1 - num2);

// Log the result
console.log(
  `The absolute difference between ${num1} and ${num2} is ${absoluteDifference}`
); // Output: The absolute difference between -5 and 5 is 10
